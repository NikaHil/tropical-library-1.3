];

// Функция для получения иконки по жанру
function getGenreIcon($genre, $genreIcons) {
    if (empty($genre)) return '📚';
    
    // Ищем точное совпадение
    if (isset($genreIcons[$genre])) {
        return $genreIcons[$genre];
    }
    
    // Если точного совпадения нет, пытаемся найти по ключевым словам
    foreach ($genreIcons as $key => $icon) {
        if (stripos($genre, $key) !== false) {
            return $icon;
        }
    }
    
    // Если ничего не нашли — стандартная иконка
    return '📖';
}
